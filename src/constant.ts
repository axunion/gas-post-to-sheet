/* eslint-disable @typescript-eslint/no-unused-vars */
const INPUT_PATTERNS = Object.freeze({
  ANY: /(?:^\S+$)|(?:^\S.*\S$)/,
  INTEGER: /(?:^0$)|(?:^-?[1-9][0-9]{0,14}$)/,
  INTEGER_UNSIGNED: /(?:^0$)|(?:^[1-9][0-9]{0,14}$)/,
  NUMERIC: /^[0-9]+$/,
  ALPHABETIC: /^[A-Za-z]+$/,
  ALPHANUMERIC: /^[0-9A-Za-z]+$/,
  ASCII: /^[\x20-\x7E]+$/,
  HIRAGANA: /^[ぁ-ん]+$/,
  KATAKANA: /^[ァ-ン]+$/,
  EMAIL:
    /^[a-zA-Z0-9.!#$%&'*+/=?^_`{|}~-]+@[a-zA-Z0-9](?:[a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?(?:\.[a-zA-Z0-9](?:[a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?)*$/,
  URI: /^https?:\/\/[^/]\S{3,1024}$/,
  PASSWORD: /^[\x21-\x7E]{8,64}$/,
  PERCENTAGE:
    /(?:^0$)|(?:^[1-9][0-9]?$)|(?:^[0-9][.][0-9]{1,2}$)|(?:^[1-9][0-9][.][0-9]{1,2}$)/,
  DATE: /^(?:[12][0-9]{3})[-](?:0[1-9]|1[0-2])[-](?:0[1-9]|[12][0-9]|3[01])$/,
  DATE_LOOSE:
    /^(?:[1-9][0-9]{0,3})[-](?:0[1-9]|1[0-2])[-](?:0[1-9]|[12][0-9]|3[01])$/,
  YEAR: /^[12][0-9]{3}$/,
  MONTH: /(?:^[1-9]$)|(?:^1[0-2]$)/,
  DAY: /(?:^[1-9]$)|(?:^[12][0-9]$)|(?:^3[01]$)/,
  COUNTRY_CODE: /^[A-Z]{2}$/,
  ONE_DIGIT: /^[0-9]$/,
  TWO_DIGIT: /^[1-9][0-9]?$/,
  FLAG: /^[0-1]$/,
});
